/**
 * RFC 9110 HTTP Semantics
 * https://www.rfc-editor.org/rfc/rfc9110.html#name-status-code-registration
 */
export enum HttpStatusCode {
  Ok200 = 200,
  Created201 = 201,
  Accepted202 = 202,
  NoContent204 = 204,
  BadInvalidRequest400 = 400,
  UnauthorizedAsInUnauthenticated401 = 401,
  ForbiddenAsInPermission403 = 403,
  NotFound404 = 404,
  Conflict409 = 409,
  Unprocessable422 = 422,
  InternalServerError500 = 500,
  ServiceUnavailable503 = 503,
}

/**
 * RFC 9457 Problem Details for HTTP APIs (obsoletes RFC 7807).
 * https://www.rfc-editor.org/rfc/rfc9457
 */
export type HttpApiProblemDetails = {
  /**
   * A URI reference [RFC3986] that identifies the problem type. This specification
   * encourages that, when dereferenced, it provide human-readable documentation for
   * the problem type (e.g., using HTML [W3C.REC-html5-20141028]). When this member
   * is not present, its value is assumed to be "about:blank".
   */
  type?: string;

  /**
   * A short, human-readable summary of the problem type.It SHOULD NOT change from
   * occurrence to occurrence of the problem, except for purposes of localization(e.g.,
   * using proactive content negotiation; see [RFC7231], Section 3.4).
   */
  title?: string;

  /**
   * The HTTP status code ([RFC7231], Section 6) generated by the origin server for
   * this occurrence of the problem.
   */
  status?: HttpStatusCode;

  /**
   * A human-readable explanation specific to this occurrence of the problem.
   */
  detail?: string;

  /**
   * A URI reference that identifies the specific occurrence of the problem.
   * It may or may not yield further information if dereferenced.
   * When the "instance" URI is not dereferenceable, it serves as a unique identifier for the
   * problem occurrence that may be of significance to the server but is opaque to the client.
   */
  instance?: string;
};
